# Store input numbers
num1 = input('Enter first number: ')
num2 = input('Enter second number: ')

# Add two numbers
sum = float(num1) + float(num2) 
#La méthode float() est utilisée pour renvoyer un nombre à virgule flottante à partir d’un nombre ou d’une chaîne.

# Display the sum
print('The sum of {0} and {1} is {2}'.format(num1, num2, sum))
